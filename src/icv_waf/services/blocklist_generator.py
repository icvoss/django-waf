def _nginx_escape_ua(pattern: str, match_type: str) -> str:
    """Format a UA pattern for inclusion in an nginx map block.

    Returns the nginx-formatted pattern string, or empty string if the pattern
    cannot be safely expressed.
    """
    pattern = pattern.strip()
    if not pattern:
        return ""

    if match_type == "exact":
        # Exact match — quote the string
        escaped = pattern.replace('"', '\\"')
        return f'"{escaped}"'

    if match_type in ("regex", "contains"):
        # nginx map uses ~* for case-insensitive regex prefix
        if match_type == "contains":
            # Wrap as a case-insensitive regex
            import re

            escaped = re.escape(pattern)
            return f'"~*{escaped}"'
        # regex — use as-is with ~* prefix
        return f'"~*{pattern}"'

    return ""
